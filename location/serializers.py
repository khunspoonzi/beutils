# -------------------------------------------------------------------------------------
# DJANGO REST FRAMEWORK IMPORTS
# -------------------------------------------------------------------------------------

from dynamic_rest.serializers import DynamicModelSerializer
from dynamic_rest.fields import DynamicRelationField

# -------------------------------------------------------------------------------------
# PROJECT IMPORTS
# -------------------------------------------------------------------------------------

from beutils.location.models import City, Country, Region, State, Subregion


# -------------------------------------------------------------------------------------
# REGION SERIALIZER
# -------------------------------------------------------------------------------------


class RegionSerializer(DynamicModelSerializer):

    # ---------------------------------------------------------------------------------
    # META
    # ---------------------------------------------------------------------------------

    class Meta:

        # -----------------------------------------------------------------------------
        # MODEL
        # -----------------------------------------------------------------------------

        model = Region
        name = "region"

        # -----------------------------------------------------------------------------
        # FIELDS
        # -----------------------------------------------------------------------------

        fields = (
            "id",
            "name",
            "emoji",
        )


# -------------------------------------------------------------------------------------
# SUBREGION SERIALIZER
# -------------------------------------------------------------------------------------


class SubregionSerializer(DynamicModelSerializer):

    # ---------------------------------------------------------------------------------
    # META
    # ---------------------------------------------------------------------------------

    class Meta:

        # -----------------------------------------------------------------------------
        # MODEL
        # -----------------------------------------------------------------------------

        model = Subregion
        name = "subregion"

        # -----------------------------------------------------------------------------
        # FIELDS
        # -----------------------------------------------------------------------------

        fields = ("id", "name", "region")


# -------------------------------------------------------------------------------------
# COUNTRY SERIALIZER
# -------------------------------------------------------------------------------------


class CountrySerializer(DynamicModelSerializer):

    # ---------------------------------------------------------------------------------
    # META
    # ---------------------------------------------------------------------------------

    class Meta:

        # -----------------------------------------------------------------------------
        # MODEL
        # -----------------------------------------------------------------------------

        model = Country
        name = "country"

        # -----------------------------------------------------------------------------
        # FIELDS
        # -----------------------------------------------------------------------------

        fields = ("id", "name", "emoji", "region", "subregion")


# -------------------------------------------------------------------------------------
# STATE SERIALIZER
# -------------------------------------------------------------------------------------


class StateSerializer(DynamicModelSerializer):

    # ---------------------------------------------------------------------------------
    # META
    # ---------------------------------------------------------------------------------

    class Meta:

        # -----------------------------------------------------------------------------
        # MODEL
        # -----------------------------------------------------------------------------

        model = State
        name = "state"

        # -----------------------------------------------------------------------------
        # FIELDS
        # -----------------------------------------------------------------------------

        fields = ("id", "name", "region", "subregion", "country")


# -------------------------------------------------------------------------------------
# CITY SERIALIZER
# -------------------------------------------------------------------------------------


class CitySerializer(DynamicModelSerializer):

    state = DynamicRelationField(StateSerializer)

    # ---------------------------------------------------------------------------------
    # META
    # ---------------------------------------------------------------------------------

    class Meta:

        # -----------------------------------------------------------------------------
        # MODEL
        # -----------------------------------------------------------------------------

        model = City
        name = "city"

        # -----------------------------------------------------------------------------
        # FIELDS
        # -----------------------------------------------------------------------------

        fields = ("id", "name", "region", "subregion", "country", "state")
